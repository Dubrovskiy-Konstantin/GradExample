        public static void RefreshInitial()
        {
            List<(string chiper, string name)> kcn = new List<(string chiper, string name)>();
            List<(string chiper, string name)> professions = new List<(string chiper, string name)>();

            try
            {
                XmlDocument xmlDcoument = new XmlDocument();
                xmlDcoument.Load(@"AppSettings.xml");
                string connectionString = xmlDcoument.SelectSingleNode("Settings").SelectSingleNode("ConnectionString").InnerText;

                using (SqlConnection connection = new SqlConnection(connectionString))
                {
                    connection.Open();
                    SqlCommand command = new SqlCommand();
                    command.Connection = connection;
                    try
                    {
                        command.CommandText = "Select * from [dbo].[Профессии];";
                        SqlDataReader reader = command.ExecuteReader();
                        while (reader.Read())
                        {
                            string name = reader.GetValue(1).ToString();
                            string chiper = reader.GetValue(2).ToString();
                            
                            if (chiper.Contains("КЦН"))
                            {
                                kcn.Add((chiper, name));
                            }
                            else
                            {
                                professions.Add((chiper, name));
                            }
                        }
                        reader.Close();
                    }
                    finally
                    {
                        connection.Close();
                    }
                }

                InitialData.Professions = professions;
                InitialData.KCN = kcn;
            }
            catch (Exception ex)
            {
                Console.WriteLine(ex.Message);
            }
        }
    }
